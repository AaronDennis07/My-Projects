        String[] temp = {in_principal_amt.getText(),in_interest_rate.getText(),in_tenure.getText()};
        all_values = convert_string_to_float(temp);
        out_pri_amt = all_values[0];
        out_interest_rate = all_values[1];
        out_tenure = all_values[2];
        
       
