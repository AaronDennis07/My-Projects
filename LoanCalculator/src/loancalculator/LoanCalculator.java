        Output_Window.Tenure.setText(String.valueOf(out_tenure));
        Output_Window.Gross_amount.setText(String.valueOf(gross_amt));
        Output_Window.Interest_Amount.setText(String.valueOf(interest_amt));
        Output_Window.vEMI.setText(String.valueOf(emi));
       
     
