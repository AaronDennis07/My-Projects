    public float[] convert_string_to_float(String[] values){
       float pri_amt = Float.parseFloat(values[0]);
       float interest_rate = Float.parseFloat(values[1]);
       float tenure = Float.parseFloat(values[2]);
       float[] float_values = {pri_amt,interest_rate,tenure};
       return float_values;
    }
